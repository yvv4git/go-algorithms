package main

import (
	"bufio"
	"fmt"
	"os"
)

func solve(N, M int, grid [][]int) (int, string) {
	/*
		METHOD: Dynamic Programming (двумерная таблица) + восстановление пути
		Суть задачи: Черепашка движется по таблице N×M, может перемещаться только вправо или вниз.
		Нужно найти максимальную сумму чисел и маршрут, на котором эта сумма достигается.

		dp[i][j] - максимальная сумма для достижения клетки (i, j).
		Переход: dp[i][j] = grid[i][j] + max(dp[i-1][j], dp[i][j-1]).

		ПОЧЕМУ ТАК:
		- В любую клетку (i, j) можно попасть только сверху (i-1, j) или слева (i, j-1)
		- Потому что движения "вверх" и "влево" запрещены
		- Значит, оптимальный путь к (i, j) = текущая клетка + максимум из двух возможных предыдущих
		- Для восстановления пути используем вспомогательную таблицу choice:
		  choice[i][j] = 'D' если пришли сверху, 'R' если пришли слева

		ПОРЯДОК ЗАПОЛНЕНИЯ:
		1. Заполняем первую ячейку dp[0][0] = grid[0][0] (стартовая точка)
		2. Заполняем первую строку: в каждой клетке (0, j) можно прийти только слева (0, j-1)
		   потому что "сверху" этой строки не существует
		3. Заполняем первый столбец: в каждой клетке (i, 0) можно прийти только сверху (i-1, 0)
		   потому что "слева" этого столбца не существует
		4. Заполняем остальные ячейки: для (i, j) выбираем max(сверху, слева)
		   потому что оба варианта возможны

		TIME COMPLEXITY: O(n*m) - проход по всем ячейкам таблицы
		SPACE COMPLEXITY: O(n*m) - двумерный массив dp и таблица выбора пути
	*/

	dp := make([][]int, N)
	for i := range dp {
		dp[i] = make([]int, M)
	}

	// Таблица для восстановления пути: 'D' - пришли сверху, 'R' - пришли слева
	choice := make([][]byte, N)
	for i := range choice {
		choice[i] = make([]byte, M)
	}

	// 1. База: первая ячейка
	dp[0][0] = grid[0][0]

	// 2. Первая строка: можно прийти только слева
	for j := 1; j < M; j++ {
		dp[0][j] = dp[0][j-1] + grid[0][j]
		choice[0][j] = 'R' // пришли слева
	}

	// 3. Первый столбец: можно прийти только сверху
	for i := 1; i < N; i++ {
		dp[i][0] = dp[i-1][0] + grid[i][0]
		choice[i][0] = 'D' // пришли сверху
	}

	// 4. Остальные ячейки: выбираем max(сверху, слева)
	for i := 1; i < N; i++ {
		for j := 1; j < M; j++ {
			if dp[i-1][j] >= dp[i][j-1] {
				dp[i][j] = dp[i-1][j] + grid[i][j]
				choice[i][j] = 'D' // пришли сверху
			} else {
				dp[i][j] = dp[i][j-1] + grid[i][j]
				choice[i][j] = 'R' // пришли слева
			}
		}
	}

	// Восстановление пути с конца к началу
	path := make([]byte, 0, N+M-2)
	i, j := N-1, M-1
	for i > 0 || j > 0 {
		path = append(path, choice[i][j])
		if choice[i][j] == 'D' {
			i--
		} else {
			j--
		}
	}
	// Разворачиваем путь (шли с конца к началу)
	for l, r := 0, len(path)-1; l < r; l, r = l+1, r-1 {
		path[l], path[r] = path[r], path[l]
	}

	return dp[N-1][M-1], string(path)
}

func main() {
	in := bufio.NewReader(os.Stdin)

	var N, M int
	fmt.Fscan(in, &N, &M)

	grid := make([][]int, N)
	for i := 0; i < N; i++ {
		grid[i] = make([]int, M)
		for j := 0; j < M; j++ {
			fmt.Fscan(in, &grid[i][j])
		}
	}

	maxSum, path := solve(N, M, grid)
	fmt.Println(maxSum)
	fmt.Println(path)
}
