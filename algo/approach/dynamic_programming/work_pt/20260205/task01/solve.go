package main

import (
	"bufio"
	"fmt"
	"os"
)

func solve(N, M int, grid [][]int) int {
	/*
		METHOD: Dynamic Programming (двумерная таблица)
		Суть задачи: Игрок движется по таблице N×M, может перемещаться только вправо или вниз.
		При прохождении через клетку снимается вес еды, записанный в этой клетке.
		Нужно найти минимальный суммарный вес для достижения правого нижнего угла.

		dp[i][j] - минимальный вес для достижения клетки (i, j).
		Переход: dp[i][j] = grid[i][j] + min(dp[i-1][j], dp[i][j-1]).

		ПОЧЕМУ ТАК:
		- В любую клетку (i, j) можно попасть только сверху (i-1, j) или слева (i, j-1)
		- Потому что движения "вверх" и "влево" запрещены
		- Значит, оптимальный путь к (i, j) = текущая клетка + минимум из двух возможных предыдущих

		ПОРЯДОК ЗАПОЛНЕНИЯ:
		1. Заполняем первую ячейку dp[0][0] = grid[0][0] (стартовая точка)
		2. Заполняем первую строку: в каждой клетке (0, j) можно прийти только слева (0, j-1)
		   потому что "сверху" этой строки не существует
		3. Заполняем первый столбец: в каждой клетке (i, 0) можно прийти только сверху (i-1, 0)
		   потому что "слева" этого столбца не существует
		4. Заполняем остальные ячейки: для (i, j) выбираем min(сверху, слева)
		   потому что оба варианта возможны

		TIME COMPLEXITY: O(n*m) - проход по всем ячейкам таблицы
		SPACE COMPLEXITY: O(n*m) - двумерный массив dp (можно оптимизировать до O(m))
	*/

	dp := make([][]int, N)
	for i := range dp {
		dp[i] = make([]int, M)
	}

	// 1. База: первая ячейка
	dp[0][0] = grid[0][0]

	// 2. Первая строка: можно прийти только слева
	for j := 1; j < M; j++ {
		dp[0][j] = dp[0][j-1] + grid[0][j]
	}

	// 3. Первый столбец: можно прийти только сверху
	for i := 1; i < N; i++ {
		dp[i][0] = dp[i-1][0] + grid[i][0]
	}

	// 4. Остальные ячейки: выбираем min(сверху, слева)
	for i := 1; i < N; i++ {
		for j := 1; j < M; j++ {
			dp[i][j] = grid[i][j] + min(dp[i-1][j], dp[i][j-1])
		}
	}

	return dp[N-1][M-1]
}

func main() {
	in := bufio.NewReader(os.Stdin)

	var N, M int
	fmt.Fscan(in, &N, &M)

	grid := make([][]int, N)
	for i := 0; i < N; i++ {
		grid[i] = make([]int, M)
		for j := 0; j < M; j++ {
			fmt.Fscan(in, &grid[i][j])
		}
	}

	fmt.Println(solve(N, M, grid))
}
