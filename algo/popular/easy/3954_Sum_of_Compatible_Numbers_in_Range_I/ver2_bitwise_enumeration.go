package main

func sumOfCompatibleNumbersV2(n, k int) int {
	/*
		TASK: Найти сумму всех положительных целых x, удовлетворяющих:
		abs(n - x) <= k и (n & x) == 0.

		METHOD: Bitwise Enumeration
		Условие (n & x) == 0 означает, что x может иметь 1 только в тех
		битовых позициях, где в n стоит 0.
		Собираем все такие позиции (до старшего бита R = n + k),
		затем перебираем все подмножества этих позиций — каждое даёт
		кандидата x. Если x попадает в [L, R] — добавляем к сумме.

		TIME COMPLEXITY: O(2^m), где m — количество нулевых бит в n
		в диапазоне [0, bits(R)].
		- bits(R) ≤ 8 (constraint: n, k ≤ 100 → R ≤ 200).
		- В худшем случае (n=0, k=100) m = 8 → 2^8 = 256 итераций.

		SPACE COMPLEXITY: O(m) для хранения списка zeroBits.
	*/
	// Из условия abs(n - x) <= k получаем диапазон [n-k, n+k].
	// При этом x должен быть положительным, поэтому нижняя граница не меньше 1.
	L := max(1, n-k) // левая граница: x положителен и abs(n-x) <= k
	R := n + k        // правая граница: abs(n-x) <= k

	// Определяем, сколько бит нужно для представления числа R (верхней границы).
	// Это нужно, чтобы знать, какие битовые позиции вообще рассматривать.
	// Например, R = 5 (101) — нужно 3 бита (позиции 0, 1, 2).
	bits := 0
	for maxVal := R; maxVal > 0; maxVal >>= 1 {
		bits++
	}

	// Собираем все битовые позиции, на которых в n стоит 0.
	// Почему: условие (n & x) == 0 означает, что у n и x не может быть
	// единиц в одной и той же позиции. Значит, x может иметь 1 только там,
	// где в n стоит 0.
	//
	// Пример: n = 2 (010 в двоичной, 3 бита).
	//   - Позиция 0 (значение 1): n имеет 0 → x может иметь 1.
	//   - Позиция 1 (значение 2): n имеет 1 → x может иметь ТОЛЬКО 0.
	//   - Позиция 2 (значение 4): n имеет 0 → x может иметь 1.
	// zeroBits = [0, 2].
	//
	// Позиции выше старшего бита n тоже считаем (n там имеет неявный 0).
	zeroBits := make([]int, 0)
	for i := 0; i < bits; i++ {
		if (n>>i)&1 == 0 {
			zeroBits = append(zeroBits, i)
		}
	}

	// m — количество позиций, где x может иметь 1.
	// Общее количество возможных значений x (без учёта диапазона) = 2^m.
	// Каждое значение получается включением или исключением каждой из m позиций.
	m := len(zeroBits)

	totalSum := 0

	// Перебираем все подмножества zeroBits.
	// Маска mask работает так:
	//   - j-й бит mask = 1 → включаем zeroBits[j] в x.
	//   - j-й бит mask = 0 → не включаем.
	//
	// Пример: zeroBits = [0, 2] (m = 2).
	// mask = 0 (00) → x = 000 = 0.
	// mask = 1 (01) → x = 001 = 1 (включили позицию 0).
	// mask = 2 (10) → x = 100 = 4 (включили позицию 2).
	// mask = 3 (11) → x = 101 = 5 (включили обе позиции).
	for mask := 0; mask < (1 << m); mask++ {
		// Собираем число x из текущего подмножества битов.
		x := 0
		for j := 0; j < m; j++ {
			// Если j-й бит маски установлен, добавляем соответствующую
			// битовую позицию zeroBits[j] в число x.
			if (mask>>j)&1 == 1 {
				x |= 1 << zeroBits[j]
			}
		}

		// Проверяем, попадает ли x в разрешённый диапазон [L, R].
		// L >= 1, так что x = 0 (пустое подмножество) автоматически отсеивается.
		if x >= L && x <= R {
			totalSum += x
		}
	}

	return totalSum
}
