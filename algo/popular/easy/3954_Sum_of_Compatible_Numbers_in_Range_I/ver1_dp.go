package main

func sumOfCompatibleNumbers(n, k int) int {
	/*
		TASK: Найти сумму всех положительных целых x, удовлетворяющих:
		abs(n - x) <= k и (n & x) == 0.

		METHOD: Digit DP
		Вычисляем f(R) - f(L-1), где f(limit) — сумма всех x из [0, limit],
		для которых (n & x) == 0.
		L = max(1, n-k), R = n + k.

		Digit DP перебирает биты от старшего к младшему.
		Состояние: (pos, tight), где tight — флаг совпадения префикса с limit.
		Возвращает: (count, sum) — количество чисел и их сумму.

		Переходы:
		- Если в n на позиции pos стоит 1, то x обязан иметь 0.
		- Если 0 — x может иметь 0 или 1 (в пределах ограничения tight).
		- При формировании суммы учитываем вклад текущего бита:
		  d * (1 << (bits - 1 - pos)) * count + sum_поддерева.

		TIME COMPLEXITY: O(bits) ~ O(log(max(n, R)))
		- Каждое состояние (pos, tight) вычисляется один раз.
		- Всего состояний: bits * 2 = O(log(max(n, R))).
		- Перебор в каждом состоянии — не более 2 цифр (0 и 1).

		SPACE COMPLEXITY: O(log(max(n, R)))
		- Хранение memo: O(bits * 2).
		- Массивы limitBits, nBits: O(bits).
		- Глубина рекурсии: O(bits).
	*/
	// Из условия abs(n - x) <= k получаем диапазон [n-k, n+k].
	// При этом x должен быть положительным, поэтому нижняя граница не меньше 1.
	L := max(1, n-k) // левая граница: x положителен и abs(n-x) <= k
	R := n + k        // правая граница: abs(n-x) <= k

	// Сумму на отрезке [L, R] считаем как разность сумм на [0, R] и [0, L-1].
	// Функция f(limit) возвращает сумму всех x от 0 до limit, где (n & x) == 0.
	return f(R, n) - f(L-1, n)
}

// f возвращает сумму всех x из [0, limit], для которых (n & x) == 0.
// Используется digit DP (цифровой динамический перебор по битам).
//
// Зачем это вообще нужно:
// Вместо того чтобы перебирать все числа от 0 до limit и проверять условие,
// мы перебираем только те числа, которые заведомо удовлетворяют (n & x) == 0,
// и дополнительно следим, чтобы не выйти за limit.
func f(limit, n int) int {
	// Если limit меньше или равен нулю, то подходящих чисел нет.
	// Не может быть ни одного x >= 0, который одновременно <= 0 и положителен,
	// так что возвращаем 0.
	if limit <= 0 {
		return 0
	}

	// Определяем, сколько бит нужно для двоичного представления чисел.
	// Берём максимум из limit (верхняя граница) и n (чтобы покрыть все биты n).
	// Например, если limit = 5 (101 в двоичной), n = 2 (10 в двоичной),
	// то max = 5, для его представления нужно 3 бита (2^2=4, 2^1=2, 2^0=1).
	bits := 0
	for maxVal := max(limit, n); maxVal > 0; maxVal >>= 1 {
		bits++
	}

	// Раскладываем limit и n на отдельные биты.
	// Массив хранит биты от старшего к младшему (MSB first),
	// чтобы перебирать их слева направо как обычные цифры числа.
	//
	// Пример: limit = 5 (101), bits = 3.
	// limitBits = [1, 0, 1]
	//   - limitBits[0] = 1 (бит со значением 4)
	//   - limitBits[1] = 0 (бит со значением 2)
	//   - limitBits[2] = 1 (бит со значением 1)
	//
	// n = 2 (010), bits = 3.
	// nBits = [0, 1, 0]
	//   - nBits[0] = 0 (n не имеет бита 4)
	//   - nBits[1] = 1 (n имеет бит 2)
	//   - nBits[2] = 0 (n не имеет бита 1)
	limitBits := make([]int, bits)
	nBits := make([]int, bits)
	for i := 0; i < bits; i++ {
		// (limit >> i) & 1 — получаем i-й бит числа limit.
		// Затем кладём его в массив так, чтобы старший бит оказался на позиции 0.
		limitBits[bits-1-i] = (limit >> i) & 1
		nBits[bits-1-i] = (n >> i) & 1
	}

	// Мемоизация: сохраняем результаты для каждой пары (pos, tight).
	// Ключ: [pos, tight], значение: [count, sum].
	// - pos: текущая позиция бита (0 = самый старший бит).
	// - tight: 1 — префикс числа совпадает с префиксом limit, дальше нельзя превышать.
	// - count: количество подходящих чисел для оставшихся битов.
	// - sum: сумма этих чисел с учётом веса их будущих битов.
	//
	// Зачем нужна мемоизация:
	// Без неё мы бы перебирали все комбинации битов — это 2^bits вариантов.
	// С мемоизацией мы переиспользуем результаты для одинаковых (pos, tight).
	// Всего состояний bits * 2, каждое считается один раз.
	memo := make(map[[2]int][2]int)

	// Рекурсивная функция DFS, которая перебирает биты числа x от старшего к младшему.
	// На каждом шаге решает: ставить 0 или 1 в текущий бит x.
	//
	// Параметры:
	//   pos   — текущая позиция (0 = самый старший бит). Когда доходим до bits, всё обработано.
	//   tight — 1 если мы пока шли ровно по limit (префикс x совпал с префиксом limit).
	//
	// Возвращает:
	//   count — сколько чисел можно составить из оставшихся битов.
	//   sum   — сумма этих чисел (вклад уже установленных старших битов НЕ учтён,
	//           он будет добавлен на уровень выше).
	var dfs func(pos, tight int) (int, int)
	dfs = func(pos, tight int) (int, int) {
		// Базовый случай: все биты обработаны.
		// Мы смогли составить одно число (пустое, без новых битов).
		// Его вклад в сумму равен 0, потому что все биты уже учтены на уровне выше.
		// Почему count = 1: это способ "собрать" одно число.
		if pos == bits {
			return 1, 0
		}

		// Проверяем, не считали ли мы уже такое состояние.
		key := [2]int{pos, tight}
		if val, ok := memo[key]; ok {
			return val[0], val[1]
		}

		// Определяем, какие значения может принимать текущий бит x.
		// Обычно это 0 или 1 (maxDigit = 1).
		// Но если tight = 1 (префикс совпал с limit дословно),
		// то мы не имеем права превысить соответствующий бит limit.
		// Например, limit = 110, мы уже поставили "11",
		// теперь на последнем бите нельзя поставить 1, потому что 111 > 110.
		maxDigit := 1
		if tight == 1 {
			maxDigit = limitBits[pos]
		}

		totalCount := 0
		totalSum := 0

		// Перебираем оба возможных значения бита: 0 и 1 (или только 0, если tight ограничивает).
		for d := 0; d <= maxDigit; d++ {
			// Условие (n & x) == 0 означает, что у n и x не должно быть
			// единиц в одних и тех же битовых позициях.
			// Если в n на этой позиции стоит 1, то в x должен быть 0.
			// Проверяем: если nBits[pos] == 1 и d == 1 — такой вариант запрещён.
			if nBits[pos] == 1 && d == 1 {
				continue
			}

			// Обновляем флаг tight для следующей позиции.
			// Если мы были в tight (префикс совпадал с limit),
			// и на текущем бите поставили цифру МЕНЬШЕ, чем в limit,
			// то теперь все следующие биты могут быть любыми (0 или 1),
			// потому что число уже гарантированно меньше limit.
			//
			// Пример: limit = 110 (биты [1,1,0]).
			// На pos=0 ставим d=1 (совпало) → tight остаётся 1.
			// На pos=1 ставим d=0 (меньше, чем 1) → tight = 0.
			// На pos=2 можно ставить и 0, и 1 — число уже меньше limit.
			newTight := tight
			if tight == 1 && d < limitBits[pos] {
				newTight = 0
			}

			// Рекурсивно обрабатываем оставшиеся биты (pos+1).
			// Получаем:
			//   cnt — сколько чисел можно составить из младших битов.
			//   sum — сумма вкладов от младших битов (без учёта текущего).
			cnt, sum := dfs(pos+1, newTight)

			// Вычисляем вес (значение) текущего бита.
			// Если bits = 3 и pos = 0 → bitValue = 1 << (3-1-0) = 1 << 2 = 4.
			// Если bits = 3 и pos = 1 → bitValue = 1 << (3-1-1) = 1 << 1 = 2.
			// Если bits = 3 и pos = 2 → bitValue = 1 << (3-1-2) = 1 << 0 = 1.
			bitValue := 1 << (bits - 1 - pos)

			// Добавляем cnt к общему количеству.
			totalCount += cnt

			// Добавляем вклад от текущего бита к общей сумме.
			// Формула: d * bitValue * cnt + sum
			// - d * bitValue — значение, которое даёт текущий бит (4, 2 или 1, умноженное на 0 или 1).
			// - d * bitValue * cnt — эта битовая величина повторяется для каждого из cnt чисел.
			// - sum — вклады всех младших битов (от рекурсии).
			//
			// Пример: bits = 3, перебираем числа от 0 до 7.
			// На pos=0 (bitValue=4), d=0: cnt=4 (числа 0,1,2,3), sum=0+1+2+3=6.
			//   Вклад = 0 * 4 * 4 + 6 = 6.
			// На pos=0 (bitValue=4), d=1: cnt=4 (числа 4,5,6,7), sum=0+1+2+3=6.
			//   Вклад = 1 * 4 * 4 + 6 = 22.
			totalSum += d*bitValue*cnt + sum
		}

		// Сохраняем результат в мемоизацию, чтобы не пересчитывать.
		memo[key] = [2]int{totalCount, totalSum}
		return totalCount, totalSum
	}

	// Запускаем DFS с самой первой позиции (pos = 0) и tight = 1.
	// tight = 1, потому что в начале префикс пустой и он совпадает с префиксом limit
	// (пустой префикс совпадает с пустым префиксом тривиально).
	// Это значит, что на старшем бите мы не можем превысить limitBits[0].
	_, sum := dfs(0, 1)

	// Возвращаем сумму всех чисел от 0 до limit, где (n & x) == 0.
	return sum
}
