package matchstickstosquare

// Задача: можно ли из всех спичек собрать квадрат?
// Каждую спичку нужно использовать ровно один раз, ломать нельзя.
//
// Пример:
//   matchsticks = [1,1,2,2,2]
//   Сумма = 1+1+2+2+2 = 8, сторона = 8/4 = 2
//   Ответ: true (две спички 1+1 = 2, две спички 2 = 2)
//
//   matchsticks = [3,3,3,3,4]
//   Сумма = 15, 15/4 = 3.75 - не делится
//   Ответ: false

// makesquareGreedy возвращает true, если можно собрать квадрат из всех спичек
func makesquareGreedy(matchsticks []int) bool {
	/*
		METHOD: Жадный подход (Greedy)

		APPROACH:
		  Жадный подход работает по принципу "на каждом шаге делаем локально оптимальный выбор".
		  В данном случае: кладём каждую спичку на самую короткую сторону, которая её вмещает.

		  1. Проверяем базовые условия (сумма делится на 4, n >= 4)
		  2. Сортируем спички по убыванию
		  3. Для каждой спички находим самую короткую сторону, которая её вмещает
		  4. Кладём спичку на эту сторону

		Почему это может работать:
		- Мы всегда используем минимально возможную сторону для текущей спички
		- Это оставляет больше места для будущих спичек

		Проблема:
		- Жадный подход НЕ ВСЕГДА даёт правильный ответ!
		- Он может зайти в тупик, даже если решение существует
		- Для гарантированного решения нужен полный перебор (backtracking)

		TIME COMPLEXITY: O(n * log n) из-за сортировки + O(n) для размещения = O(n log n)
		SPACE COMPLEXITY: O(1) дополнительно (просто массив из 4 сторон)

		Пример когда жадный НЕ работает:
		- matchsticks = [3,3,3,3,3,3,3,3,3,3,3]
		- sum = 33, target = 8.25 - не делится... ладно
		- [2,2,2,2,2,2,2,2,2,2] - sum=20, target=5
		- Жадный: 2+2 = 4, 2+2 = 4, 2+2 = 4, 2+2 = 4 (все 10 двоек)
		- Это работает! Но есть случаи когда не работает.

		ВНИМАНИЕ: Этот подход НЕ гарантирует правильный ответ для всех случаев!
		Он быстр, но может давать неверный результат.
	*/

	n := len(matchsticks)
	if n < 4 {
		return false
	}

	// Вычисляем сумму и нужную длину стороны
	total := 0
	for _, m := range matchsticks {
		total += m
	}

	// Если сумма не делится на 4, квадрат собрать нельзя
	if total%4 != 0 {
		return false
	}

	target := total / 4

	// Если хотя бы одна спичка больше нужной стороны - нельзя
	for _, m := range matchsticks {
		if m > target {
			return false
		}
	}

	// Сортируем по убыванию (обрабатываем большие спички первыми)
	sorted := make([]int, n)
	copy(sorted, matchsticks)
	for i := 0; i < n-1; i++ {
		for j := i + 1; j < n; j++ {
			if sorted[i] < sorted[j] {
				sorted[i], sorted[j] = sorted[j], sorted[i]
			}
		}
	}

	// Массив для хранения текущей длины каждой из 4 сторон
	sides := [4]int{}

	// Жадное размещение: для каждой спички находим самую короткую сторону
	for _, m := range sorted {
		// Ищем самую короткую сторону, которая может вместить эту спичку
		bestSide := -1
		minLength := target + 1 // больше максимально возможной

		for i := 0; i < 4; i++ {
			// Если спичка помещается и сторона короче текущего минимума
			if sides[i]+m <= target && sides[i] < minLength {
				minLength = sides[i]
				bestSide = i
			}
		}

		// Если не нашли подходящую сторону - жадный подход не работает
		if bestSide == -1 {
			return false
		}

		// Кладём спичку на лучшую сторону
		sides[bestSide] += m
	}

	// Проверяем, что все стороны равны target
	for i := 0; i < 4; i++ {
		if sides[i] != target {
			return false
		}
	}

	return true
}
