package matchstickstosquare

// Задача: можно ли из всех спичек собрать квадрат?
// Каждую спичку нужно использовать ровно один раз, ломать нельзя.
//
// Пример:
//   matchsticks = [1,1,2,2,2]
//   Сумма = 1+1+2+2+2 = 8, сторона = 8/4 = 2
//   Ответ: true
//
//   matchsticks = [3,3,3,3,4]
//   Сумма = 15, 15/4 = 3.75
//   Ответ: false

// makesquareDP возвращает true, если можно собрать квадрат из всех спичек
func makesquareDP(matchsticks []int) bool {
	/*
		METHOD: Bitmask DP (динамическое программирование с битовой маской)

		APPROACH:
		  Поскольку n <= 15, мы можем использовать битовую маску для представления
		  подмножеств спичек. Всего 2^n возможных подмножеств (максимум 32768).

		  Идея:
		  1. Вычисляем target = sum / 4
		  2. Находим все подмножества с суммой = target
		  3. Проверяем, можно ли выбрать 3 непересекающихся подмножества
		     (четвёртое образуется автоматически)

		Алгоритм:
		1. Предусловия: сумма делится на 4, n >= 4
		2. Создаём map: subsetSum[mask] = сумма элементов в подмножестве mask
		3. Для каждого подмножества с суммой = target, проверяем:
		   - можем ли мы выбрать 3 таких подмножества, которые не пересекаются?

		Time Complexity: O(n * 2^n) - перебираем все подмножества
		Space Complexity: O(2^n) для хранения sums

		Преимущества:
		- Использует memoization (запоминание уже вычисленных подмножеств)
		- Эффективно для small n (<= 15)

		Недостатки:
		- Требует больше памяти чем backtracking
		- Для больших n неэффективен
	*/

	n := len(matchsticks)
	if n < 4 {
		return false
	}

	// Вычисляем сумму и нужную длину стороны
	total := 0
	for _, m := range matchsticks {
		total += m
	}

	// Если сумма не делится на 4, квадрат собрать нельзя
	if total%4 != 0 {
		return false
	}

	target := total / 4

	// Если хотя бы одна спичка больше нужной стороны - нельзя
	for _, m := range matchsticks {
		if m > target {
			return false
		}
	}

	// Всего 2^n возможных подмножеств
	numSubsets := 1 << n

	// map[mask] = сумма элементов в подмножестве mask
	// mask - это битовая маска: i-й бит = 1 означает, что i-я спичка включена
	subsetSums := make([]int, numSubsets)

	// Вычисляем сумму для каждого подмножества
	// Перебираем все маски от 1 до 2^n - 1 (исключая пустое множество)
	for mask := 1; mask < numSubsets; mask++ {
		// Находим последний установленный бит (самую правую 1)
		// Используем lsb (least significant bit)
		lsb := mask & -mask // lowest set bit
		bitIndex := 0       // индекс бита

		// Находим индекс этого бита
		for i := 0; i < n; i++ {
			if (lsb >> i) == 1 {
				bitIndex = i
				break
			}
		}

		// maskWithoutLsb = mask с удалённым lsb
		maskWithoutLsb := mask ^ lsb

		// Сумма нового подмножества = сумма старого + значение элемента
		subsetSums[mask] = subsetSums[maskWithoutLsb] + matchsticks[bitIndex]
	}

	// Нам нужно найти 3 непересекающихся подмножества с суммой = target
	// Идея: используем DP для отслеживания количества сторон, которые мы можем собрать

	// dp[mask] = количество сторон, которые мы можем собрать из подмножества mask
	// Если dp[mask] = k, это значит что мы можем собрать k сторон из масок, которые
	// являются подмножествами mask
	dp := make([]int, numSubsets)

	// Инициализация: каждое подмножество с суммой = target может образовать 1 сторону
	for mask := 0; mask < numSubsets; mask++ {
		if subsetSums[mask] == target {
			dp[mask] = 1
		}
	}

	// DP: для каждой маски пробуем добавить ещё одну сторону
	for mask := 0; mask < numSubsets; mask++ {
		// Пропускаем если текущая маска уже образует 2+ сторон
		if dp[mask] >= 2 {
			continue
		}

		// Перебираем все подмаски текущей маски
		// submask - это подмножество mask
		submask := mask
		for submask > 0 {
			// Если подмаска имеет сумму = target и образует сторону
			if dp[submask] >= 1 && subsetSums[mask^submask] == target {
				// Мы можем добавить ещё одну сторону!
				dp[mask] = max(dp[mask], dp[submask]+1)
			}
			// Переходим к следующей подмаске
			submask = (submask - 1) & mask
		}
	}

	// Проверяем: можем ли мы собрать 3 стороны из полного множества?
	// Если да, то 4-я сторона соберётся автоматически
	return dp[numSubsets-1] >= 3
}

// Утилита: максимум двух чисел
func max(a, b int) int {
	if a > b {
		return a
	}
	return b
}
