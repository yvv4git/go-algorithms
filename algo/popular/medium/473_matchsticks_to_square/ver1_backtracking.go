package matchstickstosquare

// Задача: можно ли из всех спичек собрать квадрат?
// Каждую спичку нужно использовать ровно один раз, ломать нельзя.
//
// Пример:
//   matchsticks = [1,1,2,2,2]
//   Сумма = 6, сторона квадрата = 6/4 = 1.5 (не делится) - но в примере ответ true...
//   Но, сумма = 1+1+2+2+2 = 8, сторона = 8/4 = 2
//   Ответ: true (две спички 1+1 = 2, две спички 2 = 2)
//
//   matchsticks = [3,3,3,3,4]
//   Сумма = 15, 15/4 = 3.75 - не делится
//   Ответ: false

// makesquare возвращает true, если можно собрать квадрат из всех спичек
func makesquare(matchsticks []int) bool {
	/*
		METHOD: Backtracking (обратный поиск)

		APPROACH:
		  Backtracking - это метод решения задач, который использует рекурсию
		  для перебора всех возможных вариантов. Когда текущий путь не приводит
		  к решению, мы "возвращаемся" (backtrack) и пробуем другой путь.

		  В данной задаче:
		  1. Вычисляем сумму всех спичек и проверяем, делится ли она на 4
		  2. Сортируем спички по убыванию (оптимизация - большие спички первыми)
		  3. Рекурсивно пытаемся разместить каждую спичку на одной из 4 сторон
		  4. Если спичка не помещается на текущую сторону, пробуем следующую
		  5. Если ни одна сторона не подходит - возвращаемся назад (backtrack)

		  NOTE: Функция dfs() ниже - это реализация backtracking через рекурсию.
		  DFS (Depth-First Search) - это алгоритм обхода, а backtracking - это
		  метод решения, который использует рекурсию/DFS для перебора вариантов.

		TIME COMPLEXITY: O(4^n) в худшем случае, где n - количество спичек

		  Почему O(4^n)?
		  - Для каждой спички мы пробуем разместить её на одной из 4 сторон
		  - Всего n спичек, поэтому в худшем случае это 4 * 4 * 4 * ... * 4 = 4^n вариантов
		  - Но благодаря оптимизациям (сортировка, early exit) на практике работает гораздо быстрее

		  Оптимизации:
		  1. Сортировка по убыванию - большие спички пробуем первыми (быстрее отсекаем неудачные ветки)
		  2. Если сторона стала == 0, пропускаем остальные пустые стороны (избегаем симметричных решений)
		  3. Early exit - если сумма не делится на 4 или спичка больше стороны - сразу false

		SPACE COMPLEXITY: O(n) для рекурсивного стека

		Примечание: n <= 15, поэтому это решение достаточно эффективно
	*/

	n := len(matchsticks)
	if n < 4 {
		return false
	}

	// Вычисляем сумму и нужную длину стороны
	total := 0
	for _, m := range matchsticks {
		total += m
	}

	// Если сумма не делится на 4, квадрат собрать нельзя
	if total%4 != 0 {
		return false
	}

	target := total / 4

	// Если хотя бы одна спичка больше нужной стороны - нельзя
	for _, m := range matchsticks {
		if m > target {
			return false
		}
	}

	// Сортируем по убыванию (оптимизация для backtracking)
	// Большие спички сложнее разместить, поэтому пробуем их первыми
	sorted := make([]int, n)
	copy(sorted, matchsticks)
	for i := 0; i < n-1; i++ {
		for j := i + 1; j < n; j++ {
			if sorted[i] < sorted[j] {
				sorted[i], sorted[j] = sorted[j], sorted[i]
			}
		}
	}

	// Массив для хранения текущей длины каждой из 4 сторон
	sides := [4]int{}

	// Рекурсивная функция для размещения спичек
	var dfs func(index int) bool
	dfs = func(index int) bool {
		// Все спички размещены - успех!
		if index == n {
			return true
		}

		current := sorted[index]

		// Пробуем разместить текущую спичку на каждой из 4 сторон
		for i := range 4 {
			// Если спичка помещается на эту сторону
			if sides[i]+current <= target {
				sides[i] += current

				// Рекурсивно пробуем разместить следующую спичку
				if dfs(index + 1) {
					return true
				}

				// Backtrack: убираем спичку с этой стороны
				sides[i] -= current
			}

			// Оптимизация: если сторона пустая, нет смысла пробовать другие пустые стороны
			// (это предотвращает симметричные решения)
			if sides[i] == 0 {
				break
			}
		}

		return false
	}

	return dfs(0)
}
