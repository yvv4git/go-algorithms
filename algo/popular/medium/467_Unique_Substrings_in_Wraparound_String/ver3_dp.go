package main

// Оптимизированный DP с массивом: используем массив длиной 26 для каждой буквы,
// отслеживающий максимальную длину последовательности, заканчивающейся на эту букву.
// Это позволяет учесть повторяющиеся символы.
//
// Идея: для каждой позиции в строке вычисляем длину непрерывной последовательности
// (подряд идущие символы по алфавиту). Но для каждой буквы запоминаем максимальную
// длину, которая когда-либо была для неё. Это нужно, чтобы не считать одну и ту же
// подстроку несколько раз.
//
// Пример:
//   s = "aab"
//   - для 'a' на позиции 0: длина = 1, max[a] = 1
//   - для 'a' на позиции 1: длина = 2 (a->a непрерывно? нет!), длина = 1, max[a] = max(1, 1) = 1
//   - для 'b' на позиции 2: a->b подряд, длина = 2, max[b] = 2
//   Ответ = max[a] + max[b] = 1 + 2 = 3
//
//   Подстроки: "a", "a", "b", "aa", "ab", "aab"
//   Валидные в base: "a", "b", "ab" (подряд)
//   Уникальные: "a", "b", "ab" = 3
//
// Пример с повторяющимися символами:
//   s = "aa"
//   - для 'a' на позиции 0: длина = 1, max[a] = 1
//   - для 'a' на позиции 1: a->a не подряд, длина = 1, max[a] = max(1, 1) = 1
//   Ответ = max[a] = 1
//
//   Подстроки: "a", "a", "aa"
//   Валидные: только "a" (подстрока "aa" не подряд)
//   Уникальные: "a" = 1
//
// Сложность: O(n) по времени, O(1) по памяти

func findSubstringInWraproundStringDP2(s string) int {
	/*
		METHOD: Dynamic Programming (Optimized)
		APPROACH: Массив из 26 элементов хранит максимальную длину последовательности для каждой буквы, учитывая повторяющиеся символы
		TIME COMPLEXITY: O(n)
		SPACE COMPLEXITY: O(1)

		Time complexity
		Временная сложность - O(n), где n - длина строки s.
		Мы проходим по строке один раз, вычисляя длину непрерывной последовательности для каждого символа.

		Space complexity
		Пространственная сложность - O(1), так как используем массив dp из 26 элементов.
		Это оптимизированная версия, которая явно хранит максимальную длину для каждой буквы.
	*/
	n := len(s)
	if n == 0 {
		return 0
	}

	// dp[char] - максимальная длина непрерывной последовательности,
	// заканчивающейся на символ char
	dp := [26]int{}

	result := 0
	currentLen := 0

	for i := range n {
		curChar := s[i]

		// Если текущий символ продолжает последовательность (идёт подряд после предыдущего)
		if i > 0 {
			prevChar := s[i-1]
			// Проверяем: либо обычное продолжение (prev + 1 = cur), либо цикл (z -> a)
			if (prevChar == 'z' && curChar == 'a') || (curChar == prevChar+1) {
				currentLen++
			} else {
				currentLen = 1
			}
		} else {
			currentLen = 1
		}

		// Обновляем максимальную длину для текущего символа
		charIndex := curChar - 'a'
		if currentLen > dp[charIndex] {
			dp[charIndex] = currentLen
		}
	}

	// Суммируем все максимальные длины
	for i := range 26 {
		result += dp[i]
	}

	return result
}
