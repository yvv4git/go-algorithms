package wigglesubsequence

func wiggleMaxLengthOpt(nums []int) int {
	/*
		INTUITION:
		В O(n²) DP мы хранили массивы up[i] и down[i] для каждого i.
		Но на самом деле нам не нужны все предыдущие значения — достаточно
		только знать текущие up и down. Это работает, потому что при
		переходе от i-1 к i:
		- Если nums[i] > nums[i-1], то up = down + 1 (последняя разность положительная)
		- Если nums[i] < nums[i-1], то down = up + 1 (последняя разность отрицательная)
		- Если равны — ничего не меняется

		Ключевое наблюдение: для нахождения оптимальной подпоследовательности
		достаточно рассматривать только соседние элементы. Если мы пропускаем
		элементы, то "внутренние" разности просто компенсируют друг друга.

		APPROACH: Greedy / O(n) DP с O(1) памяти
		Проходим по массиву один раз, поддерживая только две переменные.

		ПОЧЕМУ ТАК:
		- Состояние определяется только текущим элементом и знаком последней разности
		- Переход зависит только от предыдущего элемента (жадное свойство)
		- Не нужно перебирать все j < i — локальный выбор оптимален глобально

		TIME COMPLEXITY: O(n) — один проход
		SPACE COMPLEXITY: O(1) — только две переменные
	*/

	n := len(nums)
	if n < 2 {
		return n
	}

	up := 1
	down := 1

	for i := 1; i < n; i++ {
		if nums[i] > nums[i-1] {
			// Положительная разность: можно продолжить последовательность,
			// заканчивавшуюся с отрицательной разностью
			up = down + 1
		} else if nums[i] < nums[i-1] {
			// Отрицательная разность: можно продолжить последовательность,
			// заканчивавшуюся с положительной разностью
			down = up + 1
		}
		// nums[i] == nums[i-1] — пропускаем, равные элементы не создают разность
	}

	return max(up, down)
}

// max возвращает максимум из двух целых чисел
func max(a, b int) int {
	if a > b {
		return a
	}
	return b
}
