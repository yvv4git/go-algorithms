package wigglesubsequence

func wiggleMaxLength(nums []int) int {
	/*
		INTUITION:
		Последовательность называется "wiggle" (волнообразной), если разности
		между соседними элементами чередуют знаки: +, -, +, -... или -, +, -, +...
		Нам нужно найти длину самой длинной такой подпоследовательности.

		Ключевое наблюдение: для каждого элемента мы можем поддерживать две величины:
		- up[i]   — максимальная длина wiggle-подпоследовательности, заканчивающейся на i-м элементе,
		            где последняя разность ПОЛОЖИТЕЛЬНАЯ (nums[i] > prev)
		- down[i] — максимальная длина wiggle-подпоследовательности, заканчивающейся на i-м элементе,
		            где последняя разность ОТРИЦАТЕЛЬНАЯ (nums[i] < prev)

		APPROACH: Dynamic Programming O(n²)
		Для каждого i рассматриваем все предыдущие j < i:
		- Если nums[i] > nums[j], то можно продолжить подпоследовательность,
		  заканчивавшуюся на j с отрицательной разностью → up[i] = max(up[i], down[j] + 1)
		- Если nums[i] < nums[j], то можно продолжить подпоследовательность,
		  заканчивавшуюся на j с положительной разностью → down[i] = max(down[i], up[j] + 1)
		- Если nums[i] == nums[j], ничего не меняется (равные элементы не дают разности)

		ПОЧЕМУ ТАК:
		- Состояние полностью определяется последним элементом и знаком последней разности
		- Переход учитывает все возможные предыдущие элементы
		- Ответ — максимум из up[n-1] и down[n-1], так как последняя разность может быть любой

		TIME COMPLEXITY: O(n^2) — два вложенных цикла
		SPACE COMPLEXITY: O(n) — два массива размера n
	*/

	n := len(nums)
	if n < 2 {
		return n
	}

	up := make([]int, n)
	down := make([]int, n)
	up[0] = 1
	down[0] = 1

	for i := 1; i < n; i++ {
		up[i] = 1
		down[i] = 1
		for j := 0; j < i; j++ {
			if nums[i] > nums[j] {
				// Положительная разность: можем продолжить последовательность,
				// заканчивавшуюся на j с отрицательной разностью
				if down[j]+1 > up[i] {
					up[i] = down[j] + 1
				}
			} else if nums[i] < nums[j] {
				// Отрицательная разность: можем продолжить последовательность,
				// заканчивавшуюся на j с положительной разностью
				if up[j]+1 > down[i] {
					down[i] = up[j] + 1
				}
			}
			// nums[i] == nums[j] — пропускаем, равные элементы не создают разность
		}
	}

	if up[n-1] > down[n-1] {
		return up[n-1]
	}
	return down[n-1]
}
