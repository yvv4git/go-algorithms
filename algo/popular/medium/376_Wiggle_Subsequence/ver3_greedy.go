package wigglesubsequence

func wiggleMaxLengthGreedy(nums []int) int {
	/*
		INTUITION:
		Волнообразная подпоследовательность соответствует чередованию "пиков" и "впадин".
		Можно просто пройти по массиву и считать количество смены направления (знака разности).

		Альтернативный взгляд: результат = 1 + количество индексов i, где
		(nums[i] - nums[i-1]) * (nums[i-1] - nums[i-2]) < 0 (смена знака).

		APPROACH: Greedy - подсчёт пиков/впадин
		Поддерживаем предыдущую ненулевую разность. При каждой смене знака увеличиваем счётчик.

		ПОЧЕМУ ТАК:
		- Каждая смена знака разности соответствует добавлению элемента в wiggle-последовательность
		- Равные соседние элементы игнорируются (разность = 0)
		- Первый и последний элементы всегда включаются

		TIME COMPLEXITY: O(n) — один проход
		SPACE COMPLEXITY: O(1) — только несколько переменных
	*/

	n := len(nums)
	if n < 2 {
		return n
	}

	// prevDiff — предыдущая ненулевая разность
	prevDiff := nums[1] - nums[0]
	// count начинается с 2, если первая разность ненулевая, иначе 1
	count := 2
	if prevDiff == 0 {
		count = 1
	}

	for i := 2; i < n; i++ {
		diff := nums[i] - nums[i-1]
		if diff == 0 {
			continue
		}
		// Если знак разности изменился (prevDiff * diff < 0)
		if prevDiff > 0 && diff < 0 || prevDiff < 0 && diff > 0 {
			count++
			prevDiff = diff
		} else if prevDiff == 0 {
			// Если предыдущая разность была 0, просто обновляем её
			prevDiff = diff
			count++
		}
		// Если знак тот же — ничего не делаем, prevDiff не меняем
		// (для жадности оставляем более "экстремальный" элемент)
	}

	return count
}