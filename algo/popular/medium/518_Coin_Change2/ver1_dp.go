package _518_Coin_Change2

func changeV1(amount int, coins []int) int {
	/*
		TASK: Подсчитать количество комбинаций, которыми можно составить сумму amount,
		используя монеты из coins неограниченное количество раз.
		Порядок монет не имеет значения (комбинации, не перестановки).
		Если сумму нельзя составить — вернуть 0.

		APPROACH: Dynamic Programming (Unbounded Knapsack)
		1. Создаем массив dp длиной amount+1, где dp[a] — количество комбинаций для суммы a.
		2. Базовый случай: dp[0] = 1 (одна комбинация — не брать ничего).
		3. Для каждой монеты проходим по суммам от coin до amount и обновляем dp:
		   dp[a] += dp[a-coin].
		4. Важно: внешний цикл по монетам, внутренний по суммам — это даёт комбинации,
		   а не перестановки (порядок монет не имеет значения).
		5. Возвращаем dp[amount].

		TIME COMPLEXITY: O(N × M)
		- N = количество монет, M = amount.
		- Два вложенных цикла: внешний по монетам, внутренний по суммам.

		SPACE COMPLEXITY: O(M)
		- Используем одномерный массив dp размером amount+1.
	*/

	dp := make([]int, amount+1)
	dp[0] = 1

	for _, coin := range coins {
		for a := coin; a <= amount; a++ {
			dp[a] += dp[a-coin]
		}
	}

	return dp[amount]
}
