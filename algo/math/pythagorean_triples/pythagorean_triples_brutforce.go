package main

import (
	"fmt"
	"math"
)

/*
Сегодня разбираемся с ежедневной задачей из литкода - нам нужно посчитать количество троек чисел (a, b, c) от 1 до n, для которых верно a^2+b^2=c^2.
Это классические Пифагоровы тройки.

Идея проста: перебираем все пары (a, b), вычисляем сумму их квадратов и проверяем,
является ли корень этой суммы целым числом не больше n.
Если да, значит тройка валидна, увеличиваем счётчик.

Временная сложность: O(n^2), так как два вложенных цикла по n.
Пространственная сложность: O(1), используем только константное количество переменных.
Почему: проверяем все возможные пары a,b, что дает квадратичное время, но эффективно для небольших n.
*/

func main() {
	// Устанавливаем примерное значение n
	n := 10
	// Вызываем функцию для подсчета троек
	result := countPythagoreanTriples(n)
	// Выводим результат
	fmt.Printf("Количество пифагоровых троек до %d: %d\n", n, result)
}

func countPythagoreanTriples(n int) int {
	// Инициализируем счетчик троек
	count := 0

	// Перебираем все возможные a от 1 до n
	for a := 1; a <= n; a++ {
		// Перебираем b от a+1 до n, чтобы a < b и избежать дубликатов
		for b := a + 1; b <= n; b++ {
			// Вычисляем сумму квадратов a и b
			sum := int64(a)*int64(a) + int64(b)*int64(b)
			// Находим целую часть квадратного корня суммы
			c := int(math.Sqrt(float64(sum)))
			// Проверяем, что c <= n, b < c (чтобы a < b < c), и c*c точно равно sum
			if c <= n && b < c && int64(c)*int64(c) == sum {
				// Если условия выполнены, увеличиваем счетчик
				count++
			}
		}
	}

	// Возвращаем общее количество найденных троек
	return count
}
