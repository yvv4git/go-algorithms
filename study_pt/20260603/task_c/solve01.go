// Задача: дана строка S. Надо вычислить Z-функцию для каждой позиции
// и вывести числа через пробел.
//
// Что подаётся на вход: одна строка из латинских букв.
// Что надо вывести: N чисел — Z[0] Z[1] ... Z[N-1], разделённые пробелом.
//
// Пример:
//   Ввод:  abracadabra
//   Вывод: 0 0 0 1 0 1 0 4 0 0 1
//
// Z-функция — для каждой позиции i считает: сколько символов подряд (начиная с i)
// совпадает с самыми первыми символами строки (с префиксом).
//
// Проще: берём строку "ababa". Смотрим на позицию 2 (буква "a"):
//   - с позиции 2 строка: "aba"
//   - начало строки:       "aba" → совпало 3 символа → Z[2] = 3
//
// Z[0] всегда равен 0 (по определению).
//
// Подход: Классический линейный алгоритм вычисления Z-функции.
// Используется [l, r] — отрезок последнего наибольшего совпадения
// для ускорения вычислений.
// Time complexity:  O(n) — каждый символ сравнивается не более одного раза.
// Space complexity: O(n) — массив Z длины n.

package main

import (
	"fmt"
	"io"
	"os"
	"strings"
)

// zFunction возвращает массив Z длины len(s).
func zFunction(s string) []int {
	n := len(s)
	z := make([]int, n)
	l, r := 0, 0
	for i := 1; i < n; i++ {
		if i <= r {
			z[i] = min(r-i+1, z[i-l])
		}
		for i+z[i] < n && s[z[i]] == s[i+z[i]] {
			z[i]++
		}
		if i+z[i]-1 > r {
			l, r = i, i+z[i]-1
		}
	}
	return z
}

func main() {
	data, _ := io.ReadAll(os.Stdin)
	s := strings.TrimSpace(string(data))

	z := zFunction(s)

	out := make([]string, len(z))
	for i, v := range z {
		out[i] = fmt.Sprint(v)
	}
	fmt.Println(strings.Join(out, " "))
}
