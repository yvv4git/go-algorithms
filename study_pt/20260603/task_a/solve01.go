// Префикс-функция (алгоритм Кнута — Морриса — Пратта)
// Подход: Префикс-функция для строки — для каждой позиции i вычисляется
// длина наибольшего собственного префикса подстроки s[0..i], совпадающего
// с её суффиксом.  Используется пересчёт через ранее вычисленные значения
// π[k-1] при несовпадении (как в автомате КМП).
// Time complexity:  O(n) — каждый символ сравнивается не более одного раза
//                   за счёт отката по π (амортизированный анализ).
// Space complexity: O(n) — массив π длины n.

package main

import (
	"fmt"
	"io"
	"os"
	"strings"
)

// prefixFunction возвращает массив π длины len(s).
// π[i] — длина наибольшего собственного префикса s[0..i],
// который также является её суффиксом.
func prefixFunction(s string) []int {
	n := len(s)
	pi := make([]int, n)
	for i := 1; i < n; i++ {
		// k — длина текущего совпадающего префикса
		k := pi[i-1]
		// Пока символы не совпадают, откатываемся
		for k > 0 && s[i] != s[k] {
			k = pi[k-1]
		}
		if s[i] == s[k] {
			k++
		}
		pi[i] = k
	}
	return pi
}

func main() {
	// Читаем всю строку целиком io.ReadAll (без ограничения буфера)
	data, _ := io.ReadAll(os.Stdin)
	s := strings.TrimSpace(string(data))

	pi := prefixFunction(s)

	// Формируем вывод через пробел
	out := make([]string, len(pi))
	for i, v := range pi {
		out[i] = fmt.Sprint(v)
	}
	fmt.Println(strings.Join(out, " "))
}
